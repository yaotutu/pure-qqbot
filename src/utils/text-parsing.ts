/**
 * 解析 QQ 表情标签为可读文本
 * 将 `<faceType=1,faceIdx=...>` 替换为 `[微笑]` 等
 */
export function parseFaceEmojis(text: string): string {
  if (!text) return text;
  return text.replace(/<faceType=(\d+)/g, (_, type: string) => {
    return FACE_MAP[type] ?? `[表情${type}]`;
  });
}

/**
 * 构建附件文本摘要
 */
export function buildAttachmentSummary(attachments?: MessageAttachment[]): string {
  if (!attachments?.length) return "";
  return attachments.map(a => {
    if (a.content_type.startsWith("image/")) return "[图片]";
    if (a.content_type.startsWith("audio/")) return `[语音${a.asr_refer_text ? `: ${a.asr_refer_text}` : ""}]`;
    if (a.content_type.startsWith("video/")) return "[视频]";
    return `[${a.content_type}]`;
  }).join(" ");
}
